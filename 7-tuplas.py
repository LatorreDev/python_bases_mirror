#!/usr/bin/env python3
"""Estudio de Tuplas"""
# Las tuplas son similares a las listas, nos permite trabajar con elementos como strings, enteros, boobleanos, etc.
# La diferencia es que son inmutables, una vez creada la tupla, no se puede modificar su contenido
# las tuplas se trabajan con parentesis ()

# Creamos una tupla
my_tuple = (1, "my string", True)
# imprimimos la tupla
print(my_tuple)
# Se puede trabajar con indices al igual que las listas
print(my_tuple[1])
# Las tuplas se pueden usar cuando se necesite tener un control total
# o que los datos sean permanentes y no vayan ca ambiar, como el path de un archivo
# Los permisos de una carpeta o el password de una base de datos
