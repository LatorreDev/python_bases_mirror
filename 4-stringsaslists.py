#!/usr/bin/env python3
"""Strings como listas"""
# Se declara la variable de tipo string
my_string = "Curso de codigo facilito"
# Se imprime el string
print(my_string)
# Se imprime el caracter de la lsita que necesito
print(my_string[0])
# Se imprime el caracter de la lsita que necesito
print(my_string[1])
# Se imprime el caracter de la lsita que necesito
print(my_string[2])
# Se imprime el caracter de la lsita que necesito
print(my_string[3])
# Se pueden recorrer los strings al contrario usando numeros negativos
# Se imprime el caracter de la lsita que necesito
print(my_string[-1])
# Se imprime el caracter de la lsita que necesito
print(my_string[-2])
# Se imprime el caracter de la lsita que necesito
print(my_string[-3])
# Se imprime el caracter de la lsita que necesito
print(my_string[-4])
# Imprimir una sublista a partir de la lista
# Se imprime la sublista usando el caracter del que parte, hasta el que necesito
print(my_string[0:15])
# Imprimir cada X posiciones una sublista a partir de una lista
print(my_string[0:15:2]) # salto de 2 en 2
# Hacer un reverse
print(my_string[::-1])
