#!/usr/bin/env python3
"""Enteros en python"""
# Existen 4 tipos de numeros en python
# Enteros: 8, 9, 10, etc. (integer)
# Flotantes(Numeros Reales) tienen punto decimal: 9.0, 5.8, 8.7, etc (float)
# Complejos: (complex)
# Booleanos: pueden ser verdadero o falso ( True or False) (boolean)
# No son mutables, un 10, siempre sera 10

# Se define primera variable con entero
number_one = 10
# Se define segunda variable
number_two = 4
# Se crea una variable que sea el resultado de la suma de las dos variables anteriores
result = number_one + number_two
# Se imprime el resultado
print("La suma es:", result)

# Se crea un nuevo resultado como la resta de las dos variables anteriores
result = number_one - number_two
# Se imprime el resultado
print("La resta es:", result)

# Se crea un nuevo resultado como la multiplicacion de las dos variables anteriores
result = number_one * number_two
# Se imprime el resultado
print("La multiplicacion es:", result)

# Se crea un nuevo resultado como la division de las dos variables anteriores
result = number_one / number_two
# Se imprime el resultado
print("La division es:", result) # Imprime un flotante

# Se crea un nuevo resultado como la division de las dos variables anteriores
result = number_one // number_two
# Se imprime el resultado
print("La division es:", result) # Imprime un entero

# Se crea un nuevo resultado como la potencia de las dos variables anteriores
result = number_one **  number_two
# Se imprime el resultado
print("La potencia es:", result)

